using UnityEngine;

namespace Unity.FPS.Game
{
    // ============================================================================
    // TimedSelfDestruct — простой таймер «убей себя через LifeTime секунд».
    // Применяется для одноразовых эффектов: частицы взрыва, шелухи и т.п.
    //
    // ВАЖНО: этот компонент УНИЧТОЖАЕТ объект (Destroy), а не возвращает в пул.
    // Для частиц/VFX в проекте уже есть PooledParticleAutoRelease — он лучше,
    // потому что не плодит мусор для GC. TimedSelfDestruct — это устаревший
    // подход, оставшийся для непуленных эффектов.
    // ============================================================================
    public class TimedSelfDestruct : MonoBehaviour
    {
        // Сколько секунд жить.
        public float LifeTime = 1f;

        // Время рождения, чтобы знать когда истечёт срок.
        float m_SpawnTime;

        // Awake — первый метод жизненного цикла, вызывается сразу при создании.
        // Раньше Start, поэтому подходит для инициализации внутреннего состояния.
        void Awake()
        {
            // Time.time — секунды с момента старта игры (масштабируется Time.timeScale).
            m_SpawnTime = Time.time;
        }

        // Каждый кадр проверяем — пора ли умереть.
        void Update()
        {
            if (Time.time > m_SpawnTime + LifeTime)
            {
                // Destroy уничтожает GameObject и все его компоненты.
                // Это создаёт нагрузку на GC. Если эта функция вызывается часто
                // (например, много снарядов) — лучше использовать пул объектов.
                Destroy(gameObject);
            }
        }
    }
}
