using UnityEngine;

namespace Unity.FPS.Game
{
    // ============================================================================
    // PooledObject — маркер, который GameObjectPoolManager автоматически
    // вешает на каждый объект, выданный из пула.
    //
    // Зачем именно так:
    // 1) Объект должен «помнить», из какого пула он пришёл — иначе при возврате
    //    мы не знаем куда его класть. Pool хранится в словаре по PrefabId.
    // 2) PrefabId — это GetInstanceID() исходного префаба. Уникален в рантайме.
    // 3) Сеттер internal — снаружи менять PrefabId нельзя, его выставляет
    //    только сам пул-менеджер при выдаче.
    // ============================================================================
    public class PooledObject : MonoBehaviour
    {
        // Идентификатор префаба-родителя. По нему пул находит нужный
        // ObjectPool<GameObject> при возврате экземпляра.
        public int PrefabId { get; internal set; }
    }
}
