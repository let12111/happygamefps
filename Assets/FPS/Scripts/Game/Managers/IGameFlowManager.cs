namespace Unity.FPS.Game
{
    // ============================================================================
    // IGameFlowManager — минимальный интерфейс «игра завершается?».
    //
    // Многим системам нужно знать только этот один флаг (например,
    // PlayerInputHandler перестаёт реагировать на ввод когда GameIsEnding=true).
    // Им не нужно знать про CanvasGroup, имена сцен и т.д.
    //
    // Принцип Interface Segregation: не вешать на потребителя то, что ему не нужно.
    // ============================================================================
    public interface IGameFlowManager
    {
        bool GameIsEnding { get; }
    }
}
